# a = 10 - setter_getter --> a adalah tipe data dengan nilai 10 - setter_getter

# Tipe data: Angka satuan (Integer)
dataInt = 10
print("Data ke-1: ", dataInt)
print("Bertipe: ", type(dataInt))

# Tipe data: Angka dengan koma (float)
dataFloat = 1.5
print("\nData ke-2: ", dataFloat)
print("Bertipe: ", type(dataFloat))

# Tipe data: Kumpulan karakter (String)
dataString = "Ucup"
print("\nData ke-3: ", dataString)
print("Bertipe: ", dataString)

