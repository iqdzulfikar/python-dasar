# 14 - Operator Assignment -> Operasi ditambah assignment

a = 5
print('Nilai a:', a)

a += 1  # a = a + 1
print('Nilai a menjadi:', a)

a -= 3  # a = a - 1
print('Nilai a menjadi:', a)

a *= 2  # a = a * 2
print('Nilai a menjadi:', a)

a /= 2  # a = a / 2
print('Nilai a menjadi:', a)

b = 10
print('\n')
print('Nilai b:', b)

b %= 3
print('Nilai b%=1 menjadi:', b)
b // 2
print('Nilai b//=2 menjadi:', b)

a = 5
print('Nilai awal a:', a)
a **= 3
print('Nilai a**=3:', a)

# Operasi Bitwise
print('\n')
