# Episode Operasi Aritmatika
x = 10
y = 3

# Operasi Penjumlahan +
hasil = x + y
print(x, '+', y, '=', hasil)

# Operasi Pengurangan -
hasil = x - y
print(x, '-', y, '=', hasil)

# Operasi Perkalian *
hasil = x * y
print(x, '*', y, '=', hasil)

# Operasi Pembagian /
hasil = x / y
print(x, '/', y, '=', hasil)

# Operasi Eksponen (Pangkat) **
hasil = x ** y
print(x, '**', y, '=', hasil)

